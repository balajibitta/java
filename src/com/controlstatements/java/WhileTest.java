package com.controlstatements.java;

public class WhileTest {

	public static void main(String[] args) {
		
		int i=1;
		while (i<=5) {
			System.out.println(i);
			i++;
		}
		System.out.println("Out of the while loop");
		
		if (i==5) {
			System.out.println("hello");
		}
		
		if (i==1) {
			System.out.println("hi");
			
		}
	}

}
